/* ------------------------------------------------------------------ *
 *  Helper macros
 * ------------------------------------------------------------------ */
#define ROL32(x, n) (((x) << (n)) | ((x) >> (32 - (n))))

/* The three f-functions from RFC 2144 Section 2.1 */
#define F1(D, Km, Kr) ({ \
    uint32_t _t = ROL32((Km) + (D), (Kr)); \
    ((S1[(_t >> 24) & 0xff]) ^ (S2[(_t >> 16) & 0xff])) - \
    (S3[(_t >>  8) & 0xff]) + (S4[(_t      ) & 0xff]); })

#define F2(D, Km, Kr) ({ \
    uint32_t _t = ROL32((Km) ^ (D), (Kr)); \
    (((S1[(_t >> 24) & 0xff]) - (S2[(_t >> 16) & 0xff])) + \
    (S3[(_t >>  8) & 0xff])) ^ (S4[(_t      ) & 0xff]); })

#define F3(D, Km, Kr) ({ \
    uint32_t _t = ROL32((Km) - (D), (Kr)); \
    (((S1[(_t >> 24) & 0xff]) + (S2[(_t >> 16) & 0xff])) ^ \
    (S3[(_t >>  8) & 0xff])) - (S4[(_t      ) & 0xff]); })

/* Big-endian 32-bit load / store */
static inline uint32_t get_be32(const uint8_t *p) {
    return ((uint32_t)p[0] << 24) | ((uint32_t)p[1] << 16) |
           ((uint32_t)p[2] <<  8) | ((uint32_t)p[3]);
}
static inline void put_be32(uint8_t *p, uint32_t v) {
    p[0] = (v >> 24) & 0xff; p[1] = (v >> 16) & 0xff;
    p[2] = (v >>  8) & 0xff; p[3] =  v        & 0xff;
}

/* ------------------------------------------------------------------ *
 *  Key schedule (RFC 2144 Section 2.4)
 *
 *  For 128-bit keys: 16 rounds; both Km[0..15] and Kr[0..15] used.
 *  The schedule is built using the CAST-128 key schedule tables
 *  embedded as S5–S8 applied to the key bytes.
 * ------------------------------------------------------------------ */
void cast5_set_key(CAST5_KEY *ks, const uint8_t *key, int keybytes)
{
    /* Zero-pad key to 16 bytes */
    uint8_t z[16] = {0};
    if (keybytes > 16) keybytes = 16;
    memcpy(z, key, (size_t)keybytes);

    /* RFC 2144 Section 2.4: derive 32 subkeys via 8 pre-key words */
    /* x0..x15 and z0..z15 are the intermediate working arrays      */
    uint32_t x[4], zz[4];

    /* Byte-index macros into z/x arrays (big-endian within each word) */
#define Z(n) (((zz[(n)>>2]) >> (8*(3-((n)&3)))) & 0xff)
#define X(n) (((x[(n)>>2]) >> (8*(3-((n)&3)))) & 0xff)

    /* Load key into x0..x3 */
    x[0] = get_be32(z +  0);
    x[1] = get_be32(z +  4);
    x[2] = get_be32(z +  8);
    x[3] = get_be32(z + 12);

    /* RFC 2144 Appendix B.2 key-schedule */
    /* Iteration 1 */
    zz[0] = x[0] ^ S5[X(13)] ^ S6[X(15)] ^ S7[X(12)] ^ S8[X(14)] ^ S7[X( 8)];
    zz[1] = x[2] ^ S5[Z( 0)] ^ S6[Z( 2)] ^ S7[Z( 1)] ^ S8[Z( 3)] ^ S8[X(10)];
    zz[2] = x[3] ^ S5[Z( 7)] ^ S6[Z( 6)] ^ S7[Z( 5)] ^ S8[Z( 4)] ^ S5[X( 9)];
    zz[3] = x[1] ^ S5[Z(10)] ^ S6[Z( 9)] ^ S7[Z(11)] ^ S8[Z( 8)] ^ S6[X(11)];
    ks->Km[ 0] = S5[Z( 8)] ^ S6[Z( 9)] ^ S7[Z( 7)] ^ S8[Z( 6)] ^ S5[Z( 2)];
    ks->Km[ 1] = S5[Z(10)] ^ S6[Z(11)] ^ S7[Z( 5)] ^ S8[Z( 4)] ^ S6[Z( 6)];
    ks->Km[ 2] = S5[Z(12)] ^ S6[Z(13)] ^ S7[Z( 3)] ^ S8[Z( 2)] ^ S7[Z( 9)];
    ks->Km[ 3] = S5[Z(14)] ^ S6[Z(15)] ^ S7[Z( 1)] ^ S8[Z( 0)] ^ S8[Z(12)];

    /* Iteration 2 */
    x[0] = zz[2] ^ S5[Z( 5)] ^ S6[Z( 7)] ^ S7[Z( 4)] ^ S8[Z( 6)] ^ S7[Z( 0)];
    x[1] = zz[0] ^ S5[X( 0)] ^ S6[X( 2)] ^ S7[X( 1)] ^ S8[X( 3)] ^ S8[Z( 2)];
    x[2] = zz[1] ^ S5[X( 7)] ^ S6[X( 6)] ^ S7[X( 5)] ^ S8[X( 4)] ^ S5[Z( 1)];
    x[3] = zz[3] ^ S5[X(10)] ^ S6[X( 9)] ^ S7[X(11)] ^ S8[X( 8)] ^ S6[Z( 3)];
    ks->Km[ 4] = S5[X( 3)] ^ S6[X( 2)] ^ S7[X(12)] ^ S8[X(13)] ^ S5[X( 8)];
    ks->Km[ 5] = S5[X( 1)] ^ S6[X( 0)] ^ S7[X(14)] ^ S8[X(15)] ^ S6[X(13)];
    ks->Km[ 6] = S5[X( 7)] ^ S6[X( 6)] ^ S7[X( 8)] ^ S8[X( 9)] ^ S7[X( 3)];
    ks->Km[ 7] = S5[X( 5)] ^ S6[X( 4)] ^ S7[X(10)] ^ S8[X(11)] ^ S8[X( 7)];

    /* Iteration 3 */
    zz[0] = x[0] ^ S5[X(13)] ^ S6[X(15)] ^ S7[X(12)] ^ S8[X(14)] ^ S7[X( 8)];
    zz[1] = x[2] ^ S5[Z( 0)] ^ S6[Z( 2)] ^ S7[Z( 1)] ^ S8[Z( 3)] ^ S8[X(10)];
    zz[2] = x[3] ^ S5[Z( 7)] ^ S6[Z( 6)] ^ S7[Z( 5)] ^ S8[Z( 4)] ^ S5[X( 9)];
    zz[3] = x[1] ^ S5[Z(10)] ^ S6[Z( 9)] ^ S7[Z(11)] ^ S8[Z( 8)] ^ S6[X(11)];
    ks->Km[ 8] = S5[Z( 3)] ^ S6[Z( 2)] ^ S7[Z(12)] ^ S8[Z(13)] ^ S5[Z( 9)];
    ks->Km[ 9] = S5[Z( 1)] ^ S6[Z( 0)] ^ S7[Z(14)] ^ S8[Z(15)] ^ S6[Z(12)];
    ks->Km[10] = S5[Z( 7)] ^ S6[Z( 6)] ^ S7[Z( 8)] ^ S8[Z( 9)] ^ S7[Z( 2)];
    ks->Km[11] = S5[Z( 5)] ^ S6[Z( 4)] ^ S7[Z(10)] ^ S8[Z(11)] ^ S8[Z( 6)];

    /* Iteration 4 */
    x[0] = zz[2] ^ S5[Z( 5)] ^ S6[Z( 7)] ^ S7[Z( 4)] ^ S8[Z( 6)] ^ S7[Z( 0)];
    x[1] = zz[0] ^ S5[X( 0)] ^ S6[X( 2)] ^ S7[X( 1)] ^ S8[X( 3)] ^ S8[Z( 2)];
    x[2] = zz[1] ^ S5[X( 7)] ^ S6[X( 6)] ^ S7[X( 5)] ^ S8[X( 4)] ^ S5[Z( 1)];
    x[3] = zz[3] ^ S5[X(10)] ^ S6[X( 9)] ^ S7[X(11)] ^ S8[X( 8)] ^ S6[Z( 3)];
    ks->Km[12] = S5[X( 8)] ^ S6[X( 9)] ^ S7[X( 7)] ^ S8[X( 6)] ^ S5[X( 3)];
    ks->Km[13] = S5[X(10)] ^ S6[X(11)] ^ S7[X( 5)] ^ S8[X( 4)] ^ S6[X( 7)];
    ks->Km[14] = S5[X(12)] ^ S6[X(13)] ^ S7[X( 3)] ^ S8[X( 2)] ^ S7[X( 8)];
    ks->Km[15] = S5[X(14)] ^ S6[X(15)] ^ S7[X( 1)] ^ S8[X( 0)] ^ S8[X(13)];

    /* Iteration 5 (rotation keys Kr) */
    zz[0] = x[0] ^ S5[X(13)] ^ S6[X(15)] ^ S7[X(12)] ^ S8[X(14)] ^ S7[X( 8)];
    zz[1] = x[2] ^ S5[Z( 0)] ^ S6[Z( 2)] ^ S7[Z( 1)] ^ S8[Z( 3)] ^ S8[X(10)];
    zz[2] = x[3] ^ S5[Z( 7)] ^ S6[Z( 6)] ^ S7[Z( 5)] ^ S8[Z( 4)] ^ S5[X( 9)];
    zz[3] = x[1] ^ S5[Z(10)] ^ S6[Z( 9)] ^ S7[Z(11)] ^ S8[Z( 8)] ^ S6[X(11)];
    ks->Kr[ 0] = (S5[Z( 8)] ^ S6[Z( 9)] ^ S7[Z( 7)] ^ S8[Z( 6)] ^ S5[Z( 2)]) & 0x1f;
    ks->Kr[ 1] = (S5[Z(10)] ^ S6[Z(11)] ^ S7[Z( 5)] ^ S8[Z( 4)] ^ S6[Z( 6)]) & 0x1f;
    ks->Kr[ 2] = (S5[Z(12)] ^ S6[Z(13)] ^ S7[Z( 3)] ^ S8[Z( 2)] ^ S7[Z( 9)]) & 0x1f;
    ks->Kr[ 3] = (S5[Z(14)] ^ S6[Z(15)] ^ S7[Z( 1)] ^ S8[Z( 0)] ^ S8[Z(12)]) & 0x1f;

    /* Iteration 6 */
    x[0] = zz[2] ^ S5[Z( 5)] ^ S6[Z( 7)] ^ S7[Z( 4)] ^ S8[Z( 6)] ^ S7[Z( 0)];
    x[1] = zz[0] ^ S5[X( 0)] ^ S6[X( 2)] ^ S7[X( 1)] ^ S8[X( 3)] ^ S8[Z( 2)];
    x[2] = zz[1] ^ S5[X( 7)] ^ S6[X( 6)] ^ S7[X( 5)] ^ S8[X( 4)] ^ S5[Z( 1)];
    x[3] = zz[3] ^ S5[X(10)] ^ S6[X( 9)] ^ S7[X(11)] ^ S8[X( 8)] ^ S6[Z( 3)];
    ks->Kr[ 4] = (S5[X( 3)] ^ S6[X( 2)] ^ S7[X(12)] ^ S8[X(13)] ^ S5[X( 8)]) & 0x1f;
    ks->Kr[ 5] = (S5[X( 1)] ^ S6[X( 0)] ^ S7[X(14)] ^ S8[X(15)] ^ S6[X(13)]) & 0x1f;
    ks->Kr[ 6] = (S5[X( 7)] ^ S6[X( 6)] ^ S7[X( 8)] ^ S8[X( 9)] ^ S7[X( 3)]) & 0x1f;
    ks->Kr[ 7] = (S5[X( 5)] ^ S6[X( 4)] ^ S7[X(10)] ^ S8[X(11)] ^ S8[X( 7)]) & 0x1f;

    /* Iteration 7 */
    zz[0] = x[0] ^ S5[X(13)] ^ S6[X(15)] ^ S7[X(12)] ^ S8[X(14)] ^ S7[X( 8)];
    zz[1] = x[2] ^ S5[Z( 0)] ^ S6[Z( 2)] ^ S7[Z( 1)] ^ S8[Z( 3)] ^ S8[X(10)];
    zz[2] = x[3] ^ S5[Z( 7)] ^ S6[Z( 6)] ^ S7[Z( 5)] ^ S8[Z( 4)] ^ S5[X( 9)];
    zz[3] = x[1] ^ S5[Z(10)] ^ S6[Z( 9)] ^ S7[Z(11)] ^ S8[Z( 8)] ^ S6[X(11)];
    ks->Kr[ 8] = (S5[Z( 3)] ^ S6[Z( 2)] ^ S7[Z(12)] ^ S8[Z(13)] ^ S5[Z( 9)]) & 0x1f;
    ks->Kr[ 9] = (S5[Z( 1)] ^ S6[Z( 0)] ^ S7[Z(14)] ^ S8[Z(15)] ^ S6[Z(12)]) & 0x1f;
    ks->Kr[10] = (S5[Z( 7)] ^ S6[Z( 6)] ^ S7[Z( 8)] ^ S8[Z( 9)] ^ S7[Z( 2)]) & 0x1f;
    ks->Kr[11] = (S5[Z( 5)] ^ S6[Z( 4)] ^ S7[Z(10)] ^ S8[Z(11)] ^ S8[Z( 6)]) & 0x1f;

    /* Iteration 8 */
    x[0] = zz[2] ^ S5[Z( 5)] ^ S6[Z( 7)] ^ S7[Z( 4)] ^ S8[Z( 6)] ^ S7[Z( 0)];
    x[1] = zz[0] ^ S5[X( 0)] ^ S6[X( 2)] ^ S7[X( 1)] ^ S8[X( 3)] ^ S8[Z( 2)];
    x[2] = zz[1] ^ S5[X( 7)] ^ S6[X( 6)] ^ S7[X( 5)] ^ S8[X( 4)] ^ S5[Z( 1)];
    x[3] = zz[3] ^ S5[X(10)] ^ S6[X( 9)] ^ S7[X(11)] ^ S8[X( 8)] ^ S6[Z( 3)];
    ks->Kr[12] = (S5[X( 8)] ^ S6[X( 9)] ^ S7[X( 7)] ^ S8[X( 6)] ^ S5[X( 3)]) & 0x1f;
    ks->Kr[13] = (S5[X(10)] ^ S6[X(11)] ^ S7[X( 5)] ^ S8[X( 4)] ^ S6[X( 7)]) & 0x1f;
    ks->Kr[14] = (S5[X(12)] ^ S6[X(13)] ^ S7[X( 3)] ^ S8[X( 2)] ^ S7[X( 8)]) & 0x1f;
    ks->Kr[15] = (S5[X(14)] ^ S6[X(15)] ^ S7[X( 1)] ^ S8[X( 0)] ^ S8[X(13)]) & 0x1f;

#undef Z
#undef X
}

/* ------------------------------------------------------------------ *
 *  Encrypt one 8-byte block (16-round version for 128-bit keys)
 * ------------------------------------------------------------------ */
void cast5_encrypt(const CAST5_KEY *ks, const uint8_t *in, uint8_t *out)
{
    uint32_t L = get_be32(in), R = get_be32(in + 4);
    uint32_t t;

    t = R; R = L ^ F1(R, ks->Km[ 0], ks->Kr[ 0]); L = t;
    t = R; R = L ^ F2(R, ks->Km[ 1], ks->Kr[ 1]); L = t;
    t = R; R = L ^ F3(R, ks->Km[ 2], ks->Kr[ 2]); L = t;
    t = R; R = L ^ F1(R, ks->Km[ 3], ks->Kr[ 3]); L = t;
    t = R; R = L ^ F2(R, ks->Km[ 4], ks->Kr[ 4]); L = t;
    t = R; R = L ^ F3(R, ks->Km[ 5], ks->Kr[ 5]); L = t;
    t = R; R = L ^ F1(R, ks->Km[ 6], ks->Kr[ 6]); L = t;
    t = R; R = L ^ F2(R, ks->Km[ 7], ks->Kr[ 7]); L = t;
    t = R; R = L ^ F3(R, ks->Km[ 8], ks->Kr[ 8]); L = t;
    t = R; R = L ^ F1(R, ks->Km[ 9], ks->Kr[ 9]); L = t;
    t = R; R = L ^ F2(R, ks->Km[10], ks->Kr[10]); L = t;
    t = R; R = L ^ F3(R, ks->Km[11], ks->Kr[11]); L = t;
    t = R; R = L ^ F1(R, ks->Km[12], ks->Kr[12]); L = t;
    t = R; R = L ^ F2(R, ks->Km[13], ks->Kr[13]); L = t;
    t = R; R = L ^ F3(R, ks->Km[14], ks->Kr[14]); L = t;
    t = R; R = L ^ F1(R, ks->Km[15], ks->Kr[15]); L = t;

    put_be32(out,     R);
    put_be32(out + 4, L);
}

/* ------------------------------------------------------------------ *
 *  Decrypt one 8-byte block (rounds in reverse order)
 * ------------------------------------------------------------------ */
void cast5_decrypt(const CAST5_KEY *ks, const uint8_t *in, uint8_t *out)
{
    uint32_t L = get_be32(in), R = get_be32(in + 4);
    uint32_t t;

    t = R; R = L ^ F1(R, ks->Km[15], ks->Kr[15]); L = t;
    t = R; R = L ^ F3(R, ks->Km[14], ks->Kr[14]); L = t;
    t = R; R = L ^ F2(R, ks->Km[13], ks->Kr[13]); L = t;
    t = R; R = L ^ F1(R, ks->Km[12], ks->Kr[12]); L = t;
    t = R; R = L ^ F3(R, ks->Km[11], ks->Kr[11]); L = t;
    t = R; R = L ^ F2(R, ks->Km[10], ks->Kr[10]); L = t;
    t = R; R = L ^ F1(R, ks->Km[ 9], ks->Kr[ 9]); L = t;
    t = R; R = L ^ F3(R, ks->Km[ 8], ks->Kr[ 8]); L = t;
    t = R; R = L ^ F2(R, ks->Km[ 7], ks->Kr[ 7]); L = t;
    t = R; R = L ^ F1(R, ks->Km[ 6], ks->Kr[ 6]); L = t;
    t = R; R = L ^ F3(R, ks->Km[ 5], ks->Kr[ 5]); L = t;
    t = R; R = L ^ F2(R, ks->Km[ 4], ks->Kr[ 4]); L = t;
    t = R; R = L ^ F1(R, ks->Km[ 3], ks->Kr[ 3]); L = t;
    t = R; R = L ^ F3(R, ks->Km[ 2], ks->Kr[ 2]); L = t;
    t = R; R = L ^ F2(R, ks->Km[ 1], ks->Kr[ 1]); L = t;
    t = R; R = L ^ F1(R, ks->Km[ 0], ks->Kr[ 0]); L = t;

    put_be32(out,     R);
    put_be32(out + 4, L);
}
